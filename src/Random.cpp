#include "Random.h"
#include "mtrand.h"

#include <ctime>
#include <cstdlib>

static MTRand drand ((unsigned long) time (0));



//-----------------------------------------------------------------------//
// Globals                                                               //
//-----------------------------------------------------------------------//

///////////////////////////////////////////////////////////////////////////
/// <summary> Generates a pseudo random integer between [0, max]. </summary>

int random (int max)
{
	double r = drand();
    int n = (int) (r * (max + 1));
    return n;
}

///////////////////////////////////////////////////////////////////////////
/// <summary> Generates a pseudo random integer between [min, max]. </summary>
int random (int min, int max)
{
	return random (max - min) + min;
}
